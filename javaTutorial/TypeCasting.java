package javaTutorial;

public class TypeCasting {

	public static void main(String[] args) {
		// Atometic Type Castinng
		byte myByte = 23;
		int myint = myByte;
		System.out.println(myByte);
		System.out.println(myint);
		// Manual TypeCasting
		float rate = 3.456f;
		int myInt = (int) rate;

		System.out.println(myInt);

	}

}
